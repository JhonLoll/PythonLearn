'''
Operadores IN e Not IN

verificam se determinado valor está em uma lista, ou
no caso das Strings que são valores iteráveis
'''

nome = 'JHON'

# Operador IN
print('O' in nome) #retornará True, se o valor estiver na String (é case-sensitive)

# Operador NOT IN
print('N' not in nome) #neste caso, o valor está presente na String, e portanto, retorna False